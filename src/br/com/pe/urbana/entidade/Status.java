package br.com.pe.urbana.entidade;

public class Status {
	
	private int id;
	private String descricao;
	
	public int getId() {
		return id;
	}
	public void setId(int valor) {
		this.id = valor;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

}
